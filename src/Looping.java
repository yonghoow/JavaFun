//package src;

public class Looping {
    public static void main(String[] args) {
        System.out.println("Increasing Order");
        for (int i = 0; i < 9; i++) {
            System.out.println(i);
        }
        System.out.println("Decreasing Order");
        for (int k = 9; k >= 0; k--) {
            System.out.println(k);
        }
    }
}
